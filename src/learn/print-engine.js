import { PDFDocument, StandardFonts, rgb } from "pdf-lib";

// AGAPAY Learn renders print-shop PDFs server-side in the Cloudflare Worker.
// pdf-lib is pure JavaScript and Worker-compatible, so we avoid Puppeteer,
// Chromium, nodejs_compat, and browser print hacks. This is the intentional
// first dependency added to support real PDF generation.

const DAYS = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];
const LETTER = [612, 792];
const MARGIN = 46;
const NAVY = rgb(0.02, 0.08, 0.15);
const NAVY_SOFT = rgb(0.06, 0.16, 0.24);
const GOLD = rgb(0.72, 0.55, 0.2);
const GOLD_SOFT = rgb(0.94, 0.86, 0.65);
const CREAM = rgb(0.98, 0.95, 0.88);
const PAPER = rgb(1, 0.99, 0.96);
const INK = rgb(0.08, 0.1, 0.14);
const MUTED = rgb(0.36, 0.38, 0.42);
const LINE = rgb(0.83, 0.78, 0.68);

function text(value) {
  return String(value ?? "").replace(/[^\x09\x0A\x0D\x20-\x7E]/g, "-").trim();
}

function sumMinutes(minutes = []) {
  return minutes.reduce((sum, minute) => sum + Number(minute || 0), 0);
}

function childFormLabel(child = {}) {
  if (child.formLabel || child.gradeLabel || child.form) return child.formLabel || child.gradeLabel || child.form;
  const years = Number(child.ageYears);
  if (!Number.isFinite(years)) return "Household Form";
  if (years <= 5) return "Little Ones";
  if (years <= 8) return "Form I";
  if (years <= 11) return "Form II";
  if (years <= 14) return "Form III";
  if (years <= 16) return "Form IV";
  return "Form V";
}

function groupChildRowsByForm(childRows = []) {
  const groups = new Map();
  childRows.forEach((row) => {
    const label = childFormLabel(row.child);
    if (!groups.has(label)) {
      groups.set(label, {
        label,
        children: new Set(),
        details: [],
        minutes: 0
      });
    }
    const group = groups.get(label);
    if (row.child?.firstName) group.children.add(row.child.firstName);
    if (row.title) group.details.push(row.detail ? `${row.title}: ${row.detail}` : row.title);
    group.minutes += sumMinutes(row.minutes);
  });
  return Array.from(groups.values());
}

function findTemplate(printCenter, templateId) {
  const requested = templateId === "weekly-pack" ? "print_mom_weekly" : templateId;
  return printCenter.templates.find((template) => template.id === requested)
    || printCenter.templates.find((template) => template.id === "print_mom_weekly")
    || printCenter.templates[0]
    || { id: "print_mom_weekly", title: "Weekly Household Plan", templateType: "weekly-household-plan" };
}

function templateWithParams(template, params = {}) {
  return {
    ...template,
    childId: params.childId || template.childId || "",
    termId: params.termId || template.termId || "",
    month: params.month || template.month || "",
    year: params.year || template.year || ""
  };
}

function childForTemplate(printCenter, template) {
  if (template.child) return template.child;
  return printCenter.children.find((child) => child.id === template.childId) || null;
}

function tableSection(heading, columns, rows) {
  return {
    type: "table",
    heading,
    columns,
    rows: rows.filter((row) => row.some((cell) => text(cell)))
  };
}

function listSection(heading, items) {
  return {
    type: "list",
    heading,
    items: items.map((item) => typeof item === "string" ? { label: item, detail: "" } : item).filter((item) => text(item.label || item.detail))
  };
}

function cardsSection(heading, items) {
  return {
    type: "cards",
    heading,
    items: items.filter((item) => text(item.label || item.value || item.detail))
  };
}

function baseDocument(printCenter, template, generatedAt) {
  return {
    title: text(template.title || "AGAPAY Learn Print Pack"),
    subtitle: text(`${printCenter.household?.name || "Household"} - ${printCenter.term?.label || "Current Term"} - ${printCenter.week?.label || ""}`),
    generatedAt,
    templateId: template.id,
    sections: [],
    footerNote: "Generated by AGAPAY Learn"
  };
}

function buildHouseholdWeekly(printCenter, template, generatedAt) {
  const document = baseDocument(printCenter, template, generatedAt);
  const week = printCenter.week || {};
  const formRows = groupChildRowsByForm(week.childRows || []);
  document.sections = [
    tableSection("Household Stream", ["Rhythm", "Notes", "Minutes"], (week.householdRows || []).map((row) => [
      row.title,
      row.detail,
      `${sumMinutes(row.minutes)}m`
    ])),
    tableSection("Form Plans", ["Form", "Children", "Assignments"], formRows.map((row) => [
      row.label,
      Array.from(row.children).join(", "),
      row.details.slice(0, 5).join("; ")
    ])),
    listSection("Liturgical Notes", (week.liturgicalDays || []).slice(0, 7).map((day) => ({
      label: `${day.weekday || ""} ${day.date || ""}`.trim(),
      detail: [day.title, day.fastingRule, day.gospelRef].filter(Boolean).join(" - ")
    })))
  ];
  return document;
}

function buildTermPlan(printCenter, template, generatedAt) {
  const document = baseDocument(printCenter, template, generatedAt);
  const setup = printCenter.termSetup || {};
  const activeTerm = (setup.termOptions || []).find((term) => term.id === setup.activeTermId) || {};
  document.sections = [
    cardsSection("Current Term", [
      { label: "Term", value: activeTerm.label || printCenter.term?.label || "Current Term", detail: printCenter.term?.week || "" },
      { label: "Calendar", value: printCenter.calendarToggle?.label || "Orthodox Calendar", detail: printCenter.calendarToggle?.description || "" },
      { label: "Grace Mode", value: "Available", detail: "Use lighter weeks without losing the term rhythm." }
    ]),
    tableSection("Children and Forms", ["Child", "Form", "Age"], (printCenter.children || []).map((child) => [
      child.firstName || child.name,
      childFormLabel(child),
      child.ageYears ? `${child.ageYears}` : ""
    ])),
    tableSection("Term Options", ["Term", "Status", "Notes"], (setup.termOptions || []).map((term) => [
      term.label,
      term.id === setup.activeTermId ? "Current" : "Planned",
      term.startDate && term.endDate ? `${term.startDate} to ${term.endDate}` : "Set in Setup"
    ]))
  ];
  return document;
}

function buildMonthCalendar(printCenter, template, generatedAt) {
  const document = baseDocument(printCenter, template, generatedAt);
  const dates = printCenter.week?.dates || [];
  document.sections = [
    tableSection("Week View", ["Day", "Date", "Feast / Rhythm", "Plan"], dates.map((date, index) => {
      const liturgicalDay = (printCenter.week?.liturgicalDays || [])[index] || {};
      const householdPlan = (printCenter.week?.householdRows || [])
        .filter((row) => Number(row.minutes?.[index] || 0) > 0)
        .map((row) => row.title)
        .slice(0, 3)
        .join(", ");
      return [DAYS[index] || "", date, liturgicalDay.title || "", householdPlan];
    })),
    listSection("Upcoming Notes", (printCenter.week?.liturgicalDays || []).map((day) => ({
      label: day.title || day.date,
      detail: [day.fastingRule, day.epistleRef, day.gospelRef].filter(Boolean).join(" - ")
    })))
  ];
  return document;
}

function buildChildWeekly(printCenter, template, generatedAt) {
  const document = baseDocument(printCenter, template, generatedAt);
  const child = childForTemplate(printCenter, template);
  const rows = (printCenter.week?.childRows || []).filter((row) => !child?.id || row.childId === child.id);
  document.title = `${child?.firstName || "Child"}'s Weekly Sheet`;
  document.sections = [
    cardsSection("Student", [
      { label: "Name", value: child?.firstName || "Student", detail: childFormLabel(child || {}) },
      { label: "Week", value: printCenter.week?.label || "Current Week", detail: printCenter.term?.label || "" }
    ]),
    tableSection("Assignments", ["Subject", "Assignment", "Minutes"], rows.map((row) => [
      row.title,
      row.detail,
      `${sumMinutes(row.minutes)}m`
    ])),
    tableSection("Daily Checkboxes", ["Subject", "Mon", "Tue", "Wed", "Thu", "Fri"], rows.map((row) => [
      row.title,
      "[ ]",
      "[ ]",
      "[ ]",
      "[ ]",
      "[ ]"
    ])),
    listSection("Parent Notes", [
      "Narration:",
      "Copywork:",
      "Prayer and habit notes:"
    ])
  ];
  return document;
}

function buildChildTerm(printCenter, template, generatedAt) {
  const document = buildChildWeekly(printCenter, template, generatedAt);
  document.title = `${childForTemplate(printCenter, template)?.firstName || "Child"}'s Term Plan`;
  document.sections = [
    ...(buildTermPlan(printCenter, template, generatedAt).sections || []),
    ...(document.sections || []).slice(1, 3)
  ];
  return document;
}

export function buildWeeklyHouseholdPrintDocument({ household, week, calendarToggle }) {
  return buildHouseholdWeekly({
    household,
    week,
    calendarToggle,
    term: { label: week?.termLabel || "Current Term" },
    templates: []
  }, { id: "print_mom_weekly", title: "Weekly Household Plan" }, new Date().toISOString());
}

export function buildLearnPrintDocument(printCenter, { templateId = "print_mom_weekly", childId = "", termId = "", month = "", year = "", generatedAt = new Date().toISOString() } = {}) {
  const template = templateWithParams(findTemplate(printCenter, templateId), { childId, termId, month, year });
  const type = template.templateType || "weekly-household-plan";
  if (type === "term-plan" || template.id === "print_mom_term") return buildTermPlan(printCenter, template, generatedAt);
  if (type === "month-calendar" || template.id === "print_mom_month") return buildMonthCalendar(printCenter, template, generatedAt);
  if (type === "liturgical-school-calendar" || template.id === "print_mom_liturgical") return buildMonthCalendar(printCenter, template, generatedAt);
  if (type === "child-weekly-assignment") return buildChildWeekly(printCenter, template, generatedAt);
  if (type === "child-term-plan") return buildChildTerm(printCenter, template, generatedAt);
  return buildHouseholdWeekly(printCenter, template, generatedAt);
}

export function buildLearnReportPrintDocument(reports, { templateId = "year-end-report", label = "", generatedAt = new Date().toISOString() } = {}) {
  const normalized = text(templateId || label || "year-end-report").toLowerCase();
  const title = normalized.includes("transcript")
    ? "Academic Transcript"
    : normalized.includes("report-card") || normalized.includes("report card")
      ? "Term Report Card"
      : normalized.includes("subject")
        ? "Subject Progress Report"
        : label || "Year-End Report";
  const document = {
    title,
    subtitle: text(`${reports.household?.name || "Household"} - ${reports.schoolYear?.label || reports.term?.label || "School Year"}`),
    generatedAt,
    templateId,
    footerNote: "Generated by AGAPAY Learn",
    sections: []
  };

  const childRows = (reports.children || []).map((child) => [
    child.firstName || child.name,
    childFormLabel(child),
    child.ageYears ? `Age ${child.ageYears}` : "",
    child.summary || ""
  ]);
  const progressRows = (reports.subjectProgress || []).map((row) => [
    row.subjectTitle,
    row.childName,
    row.formLabel,
    `${row.completed} / ${row.total} ${row.progressionType || "units"}`,
    `${row.percent}%`,
    row.status
  ]);
  const narrationRows = (reports.narrationLogs || []).slice(0, 24).map((entry) => [
    entry.date,
    entry.child?.firstName || entry.childName || entry.childId || "",
    entry.source,
    entry.narrationType || entry.type || "",
    entry.note
  ]);

  if (normalized.includes("transcript")) {
    document.sections = [
      tableSection("Transcripts", ["Student", "Grade Span", "Credits", "Status"], (reports.transcripts || []).map((transcript) => [
        transcript.child?.firstName || transcript.childId || "",
        transcript.gradeSpan || "",
        transcript.credits || "",
        transcript.status || ""
      ])),
      tableSection("Transcript Records", ["Subject", "Mark", "Model"], (reports.transcripts || []).flatMap((transcript) =>
        (transcript.records || []).map((record) => [record.subject, record.mark, record.evaluationModel])
      ))
    ];
    return document;
  }

  if (normalized.includes("report-card") || normalized.includes("report card")) {
    document.sections = [
      tableSection("Report Cards", ["Student", "Summary", "Status"], (reports.reportCards || []).map((report) => [
        report.child?.firstName || report.childId || "",
        report.summary || "",
        report.status || ""
      ])),
      tableSection("Evaluation Records", ["Subject", "Mark", "Narrative"], (reports.reportCards || []).flatMap((report) =>
        (report.records || []).map((record) => [record.subject, record.mark, record.narrativeSummary])
      ))
    ];
    return document;
  }

  document.sections = [
    cardsSection("Summary", [
      { label: "School Year", value: reports.schoolYear?.label || "", detail: reports.term?.label || "" },
      { label: "Lessons", value: `${reports.weeklySummary?.lessonsCompleted || 0} / ${reports.weeklySummary?.lessonsPlanned || 0}`, detail: "Tracked weekly progress" },
      { label: "Children", value: String((reports.children || []).length), detail: "Students in this household" }
    ]),
    tableSection("Student Overview", ["Student", "Form", "Age", "Notes"], childRows),
    tableSection("Subject Progress", ["Subject", "Student", "Form", "Progress", "Complete", "Status"], progressRows),
    tableSection("Narration Log", ["Date", "Student", "Source", "Type", "Note"], narrationRows)
  ];
  return document;
}

