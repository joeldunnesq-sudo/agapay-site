export function printDocumentFilename(document) {
  return `${text(document.title || "agapay-learn-print").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "") || "agapay-learn-print"}.pdf`;
}

function wrapText(value, font, fontSize, maxWidth) {
  const words = text(value).split(/\s+/).filter(Boolean);
  const lines = [];
  let line = "";
  words.forEach((word) => {
    const next = line ? `${line} ${word}` : word;
    if (font.widthOfTextAtSize(next, fontSize) <= maxWidth || !line) {
      line = next;
    } else {
      lines.push(line);
      line = word;
    }
  });
  if (line) lines.push(line);
  return lines.length ? lines : [""];
}

function drawTextLines(page, lines, x, y, { font, size, color, leading = size + 4 }) {
  lines.forEach((line, index) => {
    page.drawText(line, { x, y: y - index * leading, size, font, color });
  });
  return y - lines.length * leading;
}

function fitPage(state, neededHeight) {
  if (state.y - neededHeight > MARGIN + 28) return;
  addPage(state);
}

function addPage(state) {
  const page = state.pdf.addPage(LETTER);
  page.drawRectangle({ x: 0, y: 0, width: LETTER[0], height: LETTER[1], color: PAPER });
  page.drawRectangle({ x: 0, y: LETTER[1] - 78, width: LETTER[0], height: 78, color: NAVY });
  page.drawText("AGAPAY LEARN", { x: MARGIN, y: LETTER[1] - 36, size: 11, font: state.fonts.helveticaBold, color: GOLD });
  page.drawText("Print Shop", { x: MARGIN, y: LETTER[1] - 55, size: 9, font: state.fonts.helvetica, color: CREAM });
  page.drawLine({ start: { x: MARGIN, y: LETTER[1] - 86 }, end: { x: LETTER[0] - MARGIN, y: LETTER[1] - 86 }, thickness: 1, color: GOLD });
  state.page = page;
  state.pages.push(page);
  state.y = LETTER[1] - 118;
}

function drawHeading(state, heading) {
  fitPage(state, 40);
  state.page.drawText(text(heading).toUpperCase(), { x: MARGIN, y: state.y, size: 10, font: state.fonts.helveticaBold, color: GOLD });
  state.y -= 17;
  state.page.drawLine({ start: { x: MARGIN, y: state.y }, end: { x: LETTER[0] - MARGIN, y: state.y }, thickness: 0.75, color: LINE });
  state.y -= 16;
}

function drawCards(state, section) {
  drawHeading(state, section.heading);
  const cardWidth = (LETTER[0] - MARGIN * 2 - 18) / 2;
  section.items.forEach((item, index) => {
    const x = MARGIN + (index % 2) * (cardWidth + 18);
    if (index % 2 === 0) fitPage(state, 78);
    const y = state.y;
    state.page.drawRectangle({ x, y: y - 58, width: cardWidth, height: 58, borderColor: LINE, borderWidth: 0.7, color: CREAM });
    state.page.drawText(text(item.label), { x: x + 10, y: y - 18, size: 9, font: state.fonts.helveticaBold, color: GOLD });
    state.page.drawText(text(item.value || ""), { x: x + 10, y: y - 35, size: 13, font: state.fonts.timesBold, color: NAVY });
    drawTextLines(state.page, wrapText(item.detail || "", state.fonts.helvetica, 7.5, cardWidth - 20).slice(0, 2), x + 10, y - 47, {
      font: state.fonts.helvetica,
      size: 7.5,
      color: MUTED,
      leading: 9
    });
    if (index % 2 === 1 || index === section.items.length - 1) state.y -= 74;
  });
}

function drawList(state, section) {
  drawHeading(state, section.heading);
  section.items.forEach((item) => {
    const lines = wrapText([item.label, item.detail].filter(Boolean).join(" - "), state.fonts.helvetica, 9, LETTER[0] - MARGIN * 2 - 22);
    fitPage(state, lines.length * 12 + 12);
    state.page.drawText("-", { x: MARGIN + 2, y: state.y, size: 9, font: state.fonts.helveticaBold, color: GOLD });
    state.y = drawTextLines(state.page, lines, MARGIN + 18, state.y, {
      font: state.fonts.helvetica,
      size: 9,
      color: INK,
      leading: 12
    }) - 4;
  });
  state.y -= 8;
}

function drawTable(state, section) {
  drawHeading(state, section.heading);
  const widths = section.columns.map((_, index) => {
    if (section.columns.length === 3) return [130, 300, 80][index];
    if (section.columns.length === 4) return [70, 90, 170, 180][index];
    if (section.columns.length === 6) return index === 0 ? 150 : 72;
    return (LETTER[0] - MARGIN * 2) / section.columns.length;
  });
  const drawHeader = () => {
    fitPage(state, 30);
    let x = MARGIN;
    state.page.drawRectangle({ x: MARGIN, y: state.y - 18, width: LETTER[0] - MARGIN * 2, height: 22, color: NAVY_SOFT });
    section.columns.forEach((column, index) => {
      state.page.drawText(text(column), { x: x + 5, y: state.y - 10, size: 7.5, font: state.fonts.helveticaBold, color: CREAM });
      x += widths[index];
    });
    state.y -= 25;
  };
  drawHeader();
  section.rows.forEach((row) => {
    const wrapped = row.map((cell, index) => wrapText(cell, state.fonts.helvetica, 8, widths[index] - 10).slice(0, 4));
    const rowHeight = Math.max(24, Math.max(...wrapped.map((lines) => lines.length)) * 10 + 10);
    if (state.y - rowHeight < MARGIN + 32) {
      addPage(state);
      drawHeader();
    }
    let x = MARGIN;
    state.page.drawRectangle({ x: MARGIN, y: state.y - rowHeight + 4, width: LETTER[0] - MARGIN * 2, height: rowHeight, borderColor: LINE, borderWidth: 0.4, color: rgb(1, 1, 0.98) });
    wrapped.forEach((lines, index) => {
      drawTextLines(state.page, lines, x + 5, state.y - 8, {
        font: state.fonts.helvetica,
        size: 8,
        color: INK,
        leading: 10
      });
      x += widths[index];
    });
    state.y -= rowHeight;
  });
  state.y -= 12;
}

function drawSection(state, section) {
  if (!section) return;
  if (section.type === "cards") return drawCards(state, section);
  if (section.type === "table") return drawTable(state, section);
  return drawList(state, section);
}

export async function renderPrintDocumentPdf(document) {
  const pdf = await PDFDocument.create();
  const fonts = {
    times: await pdf.embedFont(StandardFonts.TimesRoman),
    timesBold: await pdf.embedFont(StandardFonts.TimesRomanBold),
    helvetica: await pdf.embedFont(StandardFonts.Helvetica),
    helveticaBold: await pdf.embedFont(StandardFonts.HelveticaBold)
  };
  const state = { pdf, fonts, pages: [], page: null, y: 0 };
  addPage(state);

  const titleLines = wrapText(document.title, fonts.timesBold, 28, LETTER[0] - MARGIN * 2);
  state.y = drawTextLines(state.page, titleLines, MARGIN, state.y, {
    font: fonts.timesBold,
    size: 28,
    color: NAVY,
    leading: 31
  });
  state.y = drawTextLines(state.page, wrapText(document.subtitle, fonts.helvetica, 10, LETTER[0] - MARGIN * 2), MARGIN, state.y - 2, {
    font: fonts.helvetica,
    size: 10,
    color: MUTED,
    leading: 13
  }) - 18;
  state.page.drawLine({ start: { x: MARGIN, y: state.y + 8 }, end: { x: LETTER[0] - MARGIN, y: state.y + 8 }, thickness: 1.2, color: GOLD_SOFT });

  document.sections.forEach((section) => drawSection(state, section));

  state.pages.forEach((page, index) => {
    page.drawLine({ start: { x: MARGIN, y: 35 }, end: { x: LETTER[0] - MARGIN, y: 35 }, thickness: 0.6, color: LINE });
    page.drawText(document.footerNote || "Generated by AGAPAY Learn", {
      x: MARGIN,
      y: 20,
      size: 8,
      font: fonts.helvetica,
      color: MUTED
    });
    page.drawText(`Page ${index + 1} of ${state.pages.length}`, {
      x: LETTER[0] - MARGIN - 60,
      y: 20,
      size: 8,
      font: fonts.helvetica,
      color: MUTED
    });
  });

  return pdf.save();
}
